# Bazel: toolchains/BUILD.bazel
# ============================================================


def _gen_toolchains_build() -> str:
    lines = [_BZL, '\npackage(default_visibility = ["//visibility:public"])\n\n']

    # ARM GNU bare-metal toolchain (must be registered before Bootlin ARM
    # so that cortexm4_baremetal resolves to arm_none_eabi, not bootlin_armv5).
    lines.append(
        "toolchain(\n"
        '    name = "arm_none_eabi",\n'
        '    toolchain = "@arm_none_eabi//:cc_toolchain",\n'
        '    toolchain_type = "@rules_cc//cc:toolchain_type",\n'
        "    target_compatible_with = [\n"
        '        "@platforms//cpu:arm",\n'
        '        "//platforms:baremetal",\n'
        "    ],\n"
        ")\n\n"
    )

    seen: set[str] = set()
    for arch in ARCHITECTURES.values():
        if arch.bootlin_arch in seen:
            continue
        seen.add(arch.bootlin_arch)
        tc_name = arch.name
        if tc_name.startswith("armv5_"):
            tc_name = "armv5"
        elif tc_name.startswith("armv7_"):
            tc_name = "armv7"
        lines.append(
            f"toolchain(\n"
            f'    name = "bootlin_{tc_name}",\n'
            f'    toolchain = "@bootlin_{tc_name}//:cc_toolchain",\n'
            f'    toolchain_type = "@rules_cc//cc:toolchain_type",\n'
            f'    target_compatible_with = ["{arch.cpu_constraint}"],\n'
            f")\n\n"
        )
    return "".join(lines)


# ============================================================
# .bazelrc platform configs (injected between markers)
# ============================================================

_MARKER_START = "# BEGIN GENERATED PLATFORM CONFIGS"
_MARKER_END = "# END GENERATED PLATFORM CONFIGS"


def _gen_bazelrc_block() -> str:
    lines = []
    for os_name, os_def in OPERATING_SYSTEMS.items():
        for arch_name in os_def.architectures:
            cfg = f"{os_name}_{arch_name}"
            pad = max(1, 22 - len(cfg))
            lines.append(f"build:{cfg}{' ' * pad}--platforms=//platforms:{cfg}")
        lines.append("")
    return "\n".join(lines)


# ============================================================
# Bazel: bazel/platforms.bzl — BLOB_TARGETS dict
# ============================================================


def _gen_platforms_bzl() -> str:
    lines = [
        _BZL,
        textwrap.dedent("""\

        \"\"\"Platform target map — single source of truth for OS/arch combinations.

        Loaded by blob.bzl and other rules that need the full platform matrix.
        \"\"\"

        BLOB_TARGETS = {
        """),
    ]
    for os_name, os_def in OPERATING_SYSTEMS.items():
        for arch_name in os_def.architectures:
            key = f"{os_name}:{arch_name}"
            val = f"//platforms:{os_name}_{arch_name}"
            lines.append(f'    "{key}": "{val}",\n')
    lines.append("}\n")
    return "".join(lines)


# ============================================================
# Blob auto-discovery: payload BUILD.bazel
# ============================================================


def _blob_needs_hosted(source: Path) -> bool:
    """Check if a blob source uses the hosted platform abstraction."""
    content = source.read_text()
    return "PIC_PLATFORM_HOSTED" in content


def _gen_payload_build() -> str:
    """Generate src/payload/BUILD.bazel by scanning for .c files."""
    payload_dir = PROJECT_ROOT / "src/payload"
    c_files = sorted(f.stem for f in payload_dir.glob("*.c"))
    if not c_files:
        return ""

    # Check which blobs need hosted variants and extract_bin.
    hosted_blobs = [s for s in c_files if _blob_needs_hosted(payload_dir / f"{s}.c")]

    lines = [
        _BZL,
        textwrap.dedent("""\

        load("//bazel:blob.bzl", "pic_blob")
        """),
    ]

    # Only load extract_bin if there are blobs that need it.
    if hosted_blobs:
        lines.append('load("//src/payload:extract.bzl", "extract_bin")\n')

    lines.append(
        textwrap.dedent("""\

        package(default_visibility = ["//visibility:public"])

        """)
    )

    for stem in c_files:
        lines.append(
            textwrap.dedent(f"""\
            pic_blob(
                name = "{stem}",
                srcs = ["{stem}.c"],
                deps = ["//src/include/picblobs:headers"],
                linker_script = "//src/linker:blob.ld",
            )

            """)
        )

        if stem in hosted_blobs:
            lines.append(
                textwrap.dedent(f"""\
                extract_bin(
                    name = "{stem}_bin",
                    src = ":{stem}",
                )

                pic_blob(
                    name = "{stem}_hosted",
                    srcs = ["{stem}.c"],
                    local_defines = ["PIC_PLATFORM_HOSTED"],
                    deps = ["//src/include/picblobs:headers"],
                    linker_script = "//src/linker:blob.ld",
                )

                extract_bin(
                    name = "{stem}_hosted_bin",
                    src = ":{stem}_hosted",
                )

                """)
            )

    return "".join(lines)


# ============================================================
# Main
# ============================================================


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate derived files from registry")
    parser.add_argument(
        "--check",
        action="store_true",
        help="Verify generated files are up to date (exit 1 if not)",
    )
    args = parser.parse_args()

    targets: list[tuple[Path, str]] = [
        # C headers
        (PROJECT_ROOT / "src/include/picblobs/arch.h", _gen_arch_h()),
        (PROJECT_ROOT / "src/include/picblobs/syscall.h", _gen_syscall_h()),
        (PROJECT_ROOT / "src/include/picblobs/picblobs.h", _gen_picblobs_h()),
        # Bazel
        (PROJECT_ROOT / "platforms/BUILD.bazel", _gen_platforms_build()),
        (PROJECT_ROOT / "toolchains/BUILD.bazel", _gen_toolchains_build()),
        (PROJECT_ROOT / "bazel/platforms.bzl", _gen_platforms_bzl()),
        # Blob targets
        (PROJECT_ROOT / "src/payload/BUILD.bazel", _gen_payload_build()),
    ]

    # Per-syscall sys/{name}.h files.
    for sdef in SYSCALL_DEFS.values():
        path = PROJECT_ROOT / f"src/include/picblobs/sys/{sdef.name}.h"
        targets.append((path, _gen_syscall_header(sdef)))

    # Per-OS runner.c files.
    # The Windows runner is hand-written (mock TEB/PEB, not a generic loader)
    # and must NOT be overwritten by the generator.
    for os_name in OPERATING_SYSTEMS:
        if os_name == "windows":
            continue
        runner_c = _gen_runner_c(os_name)
        if runner_c:
            path = PROJECT_ROOT / f"tests/runners/{os_name}/runner.c"
            targets.append((path, runner_c))

    any_changed = False
    for path, content in targets:
        if not content:
            continue
        if _write(path, content, args.check):
            any_changed = True

    # .bazelrc: inject between markers.
    bazelrc_path = PROJECT_ROOT / ".bazelrc"
    bazelrc = bazelrc_path.read_text()
    block = f"{_MARKER_START}\n{_gen_bazelrc_block()}{_MARKER_END}"

    if _MARKER_START in bazelrc:
        new_bazelrc = re.sub(
            rf"{re.escape(_MARKER_START)}.*?{re.escape(_MARKER_END)}",
            block,
            bazelrc,
            flags=re.DOTALL,
        )
    else:
        # First run: find platform config lines and replace them.
        lines = bazelrc.split("\n")
        start = end = None
        for i, line in enumerate(lines):
            if re.match(r"build:(linux|freebsd|windows)_", line):
                if start is None:
                    start = i
                end = i
        if start is not None:
            new_bazelrc = "\n".join(lines[:start] + [block] + lines[end + 1 :])
        else:
            new_bazelrc = bazelrc + "\n" + block + "\n"

    if _write(bazelrc_path, new_bazelrc, args.check):
        any_changed = True

    if args.check and any_changed:
        print("\nOut of date. Run: python tools/generate.py")
        return 1
    if not any_changed:
        print("All generated files up to date.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
