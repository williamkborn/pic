# C: runner.c — generated dispatcher to per-arch start/*.h
# ============================================================


def _gen_runner_c(os_name: str) -> str:
    start_dir = PROJECT_ROOT / "tests/runners" / os_name / "start"
    if not start_dir.exists():
        return ""  # OS runner not yet implemented

    lines = [
        _C,
        textwrap.dedent(f"""\
        /*
         * {os_name.title()} test runner for PIC blobs.
         *
         * Loads a blob binary into an RWX memory region and transfers
         * execution to offset 0. Per-architecture _start stubs live in
         * start/{{arch}}.h — add a new file there for new architectures.
         */

        #include "picblobs/os/{os_name}.h"
        #include "picblobs/sys/read.h"
        #include "picblobs/sys/open.h"
        #include "picblobs/sys/close.h"
        #include "picblobs/sys/lseek.h"
        #include "picblobs/sys/mmap.h"
        #include "picblobs/sys/mprotect.h"
        #include "picblobs/sys/exit.h"
        #include "picblobs/sys/exit_group.h"

        #define RUNNER_ERROR 127

        static long file_size(int fd) {{
            long end = pic_lseek(fd, 0, PIC_SEEK_END);
            if (end < 0) {{
                return -1;
            }}
            if (pic_lseek(fd, 0, PIC_SEEK_SET) < 0) {{
                return -1;
            }}
            return end;
        }}

        static long read_all(int fd, void *buf, pic_size_t count) {{
            pic_u8 *p = (pic_u8 *)buf;
            pic_size_t done = 0;
            while (done < count) {{
                long n = pic_read(fd, p + done, count - done);
                if (n <= 0) {{
                    return -1;
                }}
                done += (pic_size_t)n;
            }}
            return (long)done;
        }}

        /* Per-architecture _start — dispatched to start/{{arch}}.h files. */

        """),
    ]

    # Discover available start/*.h files and map to gcc defines.
    arch_files: list[tuple[str, str]] = []
    for gcc_define in _sorted_gcc_defines():
        stem = gcc_define.strip("_")
        start_file = start_dir / f"{stem}.h"
        if start_file.exists():
            arch_files.append((gcc_define, stem))

    for i, (gcc_define, stem) in enumerate(arch_files):
        prefix = "#if" if i == 0 else "#elif"
        lines.append(f"{prefix} defined({gcc_define})\n")
        lines.append(f'#include "start/{stem}.h"\n\n')

    lines.append(
        textwrap.dedent("""\
        #else
        #error "Unsupported architecture for _start — add start/{arch}.h"
        #endif

        int runner_main(int argc, char **argv) {
            if (argc < 2) {
                pic_exit_group(RUNNER_ERROR);
            }

            int fd = (int)pic_open(argv[1], PIC_O_RDONLY, 0);
            if (fd < 0) {
                pic_exit_group(RUNNER_ERROR);
            }

            long size = file_size(fd);
            if (size <= 0) {
                pic_close(fd);
                pic_exit_group(RUNNER_ERROR);
            }

            void *mem = pic_mmap(
                PIC_NULL, (pic_size_t)size,
                PIC_PROT_READ | PIC_PROT_WRITE | PIC_PROT_EXEC,
                PIC_MAP_PRIVATE | PIC_MAP_ANONYMOUS,
                -1, 0
            );
            if ((long)mem == -1) {
                pic_close(fd);
                pic_exit_group(RUNNER_ERROR);
            }

            if (read_all(fd, mem, (pic_size_t)size) < 0) {
                pic_close(fd);
                pic_exit_group(RUNNER_ERROR);
            }

            pic_close(fd);

            /* On ARM Thumb, function pointers must have bit 0 set to stay
             * in Thumb mode. The mmap'd address is page-aligned (bit 0 = 0),
             * so without this fixup BLX would switch to ARM mode. */
        #ifdef __thumb__
            ((void (*)(void))((pic_uintptr)mem | 1))();
        #else
            ((void (*)(void))mem)();
        #endif

            pic_exit_group(RUNNER_ERROR);
        }
        """)
    )
    return "".join(lines)


