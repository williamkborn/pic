# ============================================================
# Helpers
# ============================================================


def _sorted_gcc_defines() -> list[str]:
    """Return gcc defines in a stable order for #if/#elif chains."""
    # Use insertion order from ARCHITECTURES (deterministic).
    seen: list[str] = []
    for arch in ARCHITECTURES.values():
        if arch.gcc_define not in seen:
            seen.append(arch.gcc_define)
    return seen


def _clang_format(content: str) -> str:
    """Run clang-format on content if available. Returns formatted content."""
    import shutil

    if not shutil.which("clang-format"):
        return content
    try:
        result = subprocess.run(
            [
                "clang-format",
                f"--assume-filename={PROJECT_ROOT}/x.c",
                f"--style=file:{PROJECT_ROOT}/.clang-format",
            ],
            input=content,
            capture_output=True,
            text=True,
        )
        if result.returncode == 0:
            return result.stdout
    except Exception:
        pass
    return content


def _write(path: Path, content: str, check: bool) -> bool:
    """Write content to path. Returns True if file changed.

    C/H files are run through clang-format before writing.
    """
    if path.suffix in (".c", ".h"):
        content = _clang_format(content)
    if path.exists() and path.read_text() == content:
        return False
    if check:
        print(f"  OUT OF DATE: {path.relative_to(PROJECT_ROOT)}")
        return True
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content)
    print(f"  wrote {path.relative_to(PROJECT_ROOT)}")
    return True


