# C: arch.h — generated from registry traits
# ============================================================


def _gen_arch_h() -> str:
    lines = [
        _C,
        textwrap.dedent("""\
        /*
         * picblobs/arch.h — architecture trait flags.
         *
         * Centralizes all per-architecture boolean decisions into named traits.
         * Other headers check traits (e.g., PIC_ARCH_USES_MMAP2) instead of
         * raw architecture macros.
         *
         * To add a new architecture: add an entry to tools/registry.py,
         * then run: python tools/generate.py
         */

        #ifndef PICBLOBS_ARCH_H
        #define PICBLOBS_ARCH_H
        """),
    ]

    trait_names = [
        "uses_mmap2",
        "uses_old_mmap",
        "openat_only",
        "needs_got_reloc",
        "needs_trampoline",
        "is_32bit",
    ]
    trait_macros = {t: f"PIC_ARCH_{t.upper()}" for t in trait_names}

    defines = _sorted_gcc_defines()
    for i, gcc_define in enumerate(defines):
        # Find the first arch with this define (for name/bits).
        arch = next(a for a in ARCHITECTURES.values() if a.gcc_define == gcc_define)
        prefix = "#if" if i == 0 else "#elif"
        lines.append(f"{prefix} defined({gcc_define})\n")
        lines.append(f'#define PIC_ARCH_NAME            "{arch.name.split("_")[0]}"\n')
        lines.append(
            f"#define PIC_ARCH_BITS            {64 if not arch.is_32bit else 32}\n"
        )
        for trait in trait_names:
            if getattr(arch, trait, False):
                lines.append(f"#define {trait_macros[trait]:<28s} 1\n")
        lines.append("\n")

    lines.append(
        textwrap.dedent("""\
        #else
        #error "Unsupported architecture — add to tools/registry.py and run generate.py"
        #endif

        /* Default trait values for architectures that don't set them. */
        """)
    )
    for trait in trait_names:
        macro = trait_macros[trait]
        lines.append(f"#ifndef {macro}\n#define {macro:<28s} 0\n#endif\n")

    lines.append("\n#endif /* PICBLOBS_ARCH_H */\n")
    return "".join(lines)


# ============================================================
# C: syscall.h — generated dispatcher to per-arch files
# ============================================================


def _gen_syscall_h() -> str:
    lines = [
        _C,
        textwrap.dedent("""\
        /*
         * picblobs/syscall.h — architecture-portable syscall interface.
         *
         * Dispatches to per-architecture implementations in picblobs/syscall/.
         * To add a new architecture: create picblobs/syscall/{name}.h with
         * pic_raw_syscall(), add to registry.py, run generate.py.
         */

        #ifndef PICBLOBS_SYSCALL_H
        #define PICBLOBS_SYSCALL_H

        #include "picblobs/types.h"

        /*
         * Per-architecture pic_raw_syscall() — one file per unique GCC define.
         * Signature: long pic_raw_syscall(long number, long a0..a5)
         */

        """),
    ]

    defines = _sorted_gcc_defines()
    # Map gcc_define to the file stem (strip underscores).
    define_to_file = {}
    for arch in ARCHITECTURES.values():
        if arch.gcc_define not in define_to_file:
            # __x86_64__ → x86_64, __i386__ → i386, __arm__ → arm, __mips__ → mips
            stem = arch.gcc_define.strip("_")
            define_to_file[arch.gcc_define] = stem

    for i, gcc_define in enumerate(defines):
        prefix = "#if" if i == 0 else "#elif"
        stem = define_to_file[gcc_define]
        lines.append(f"{prefix} defined({gcc_define})\n")
        lines.append(f'#include "picblobs/syscall/{stem}.h"\n\n')

    lines.append(
        textwrap.dedent("""\
        #else
        #error "Unsupported architecture for pic_raw_syscall — add picblobs/syscall/{arch}.h"
        #endif

        /* Convenience wrappers. */
        #define pic_syscall0(n)                     pic_raw_syscall((n), 0, 0, 0, 0, 0, 0)
        #define pic_syscall1(n, a)                  pic_raw_syscall((n), (long)(a), 0, 0, 0, 0, 0)
        #define pic_syscall2(n, a, b)               pic_raw_syscall((n), (long)(a), (long)(b), 0, 0, 0, 0)
        #define pic_syscall3(n, a, b, c)            pic_raw_syscall((n), (long)(a), (long)(b), (long)(c), 0, 0, 0)
        #define pic_syscall4(n, a, b, c, d)         pic_raw_syscall((n), (long)(a), (long)(b), (long)(c), (long)(d), 0, 0)
        #define pic_syscall5(n, a, b, c, d, e)      pic_raw_syscall((n), (long)(a), (long)(b), (long)(c), (long)(d), (long)(e), 0)
        #define pic_syscall6(n, a, b, c, d, e, f)   pic_raw_syscall((n), (long)(a), (long)(b), (long)(c), (long)(d), (long)(e), (long)(f))

        #endif /* PICBLOBS_SYSCALL_H */
        """)
    )
    return "".join(lines)


# ============================================================
# C: picblobs.h — auto-discovers sys/*.h wrappers
# ============================================================


def _gen_picblobs_h() -> str:
    sys_dir = PROJECT_ROOT / "src/include/picblobs/sys"
    wrappers = sorted(
        f.name
        for f in sys_dir.glob("*.h")
        if f.name != "constants.h"  # not a syscall wrapper
    )

    # picblobs.h now includes syscall.h (for the primitive) but NOT
    # individual sys/*.h wrappers — those require an OS header first.
    # The blob author includes: os/{os}.h + picblobs.h, OR os/{os}.h + individual sys/*.h.
    syscall_names = sorted(SYSCALL_DEFS.keys())
    includes = "\n".join(f'#include "picblobs/sys/{n}.h"' for n in syscall_names)
    return textwrap.dedent(f"""\
        {_C}/*
         * picblobs/picblobs.h — single include for blob authors.
         *
         * Usage:
         *   #include "picblobs/os/linux.h"     // select target OS
         *   #include "picblobs/picblobs.h"     // everything else
         */

        #ifndef PICBLOBS_PICBLOBS_H
        #define PICBLOBS_PICBLOBS_H

        #include "picblobs/arch.h"
        #include "picblobs/types.h"
        #include "picblobs/syscall.h"
        #include "picblobs/section.h"
        #include "picblobs/reloc.h"

        /* Syscall wrappers (each self-contained with numbers + function). */
        {includes}

        #endif /* PICBLOBS_PICBLOBS_H */
    """)


# ============================================================
# C: sys/{name}.h — one self-contained file per syscall
# ============================================================


def _gen_syscall_header(sdef: SyscallDef) -> str:
    """Generate a complete sys/{name}.h with numbers + wrapper."""
    guard = f"PICBLOBS_SYS_{sdef.name.upper()}_H"
    os_support = syscall_os_support(sdef.name)
    supported_oses = sorted(os_support.keys())

    lines: list[str] = [_C]
    lines.append(f"/* picblobs/sys/{sdef.name}.h — {sdef.wrapper}() syscall. */\n")
    lines.append(f"/* Supported: {', '.join(supported_oses)} */\n\n")
    lines.append(f"#ifndef {guard}\n#define {guard}\n\n")
    lines.append(f'#include "picblobs/types.h"\n\n')

    # Hosted-mode path: delegate to pic_platform vtable.
    if sdef.hosted_call:
        lines.append("#ifdef PIC_PLATFORM_HOSTED\n")
        lines.append('#include "picblobs/platform.h"\n')
        attrs = ""
        if sdef.noreturn:
            attrs = "__attribute__((noreturn))\n"
        lines.append(
            f"{attrs}static inline {sdef.return_type} {sdef.wrapper}({sdef.params}) {{\n"
        )
        if sdef.return_type == "void":
            lines.append(f"\t{sdef.hosted_call};\n")
            if sdef.noreturn:
                lines.append("\t__builtin_unreachable();\n")
        else:
            lines.append(f"\treturn {sdef.hosted_call};\n")
        lines.append("}\n")
        lines.append("#else /* !PIC_PLATFORM_HOSTED */\n\n")

    lines.append(f'#include "picblobs/arch.h"\n')
    lines.append(f'#include "picblobs/syscall.h"\n\n')

    # --- OS dispatch + per-arch numbers ---
    # Collect all __NR_ names this syscall needs (for open: open + openat).
    nr_names_needed: set[str] = set()
    for os_name, arch_nrs in os_support.items():
        for key in arch_nrs:
            if key == "_all_":
                nr_names_needed.add(sdef.name)
            else:
                # The key in the table might differ from sdef.name
                # (e.g., open has "openat" on aarch64).
                # Look up what's actually in the table.
                pass
        # Collect actual nr names from the OS tables.
        if "_all_" in arch_nrs:
            nr_names_needed.add(sdef.name)
        else:
            for gcc_define, _ in arch_nrs.items():
                # Check if this arch defines this syscall under a different name.
                os_table = SYSCALL_NUMBERS[os_name]
                for name in os_table.get(gcc_define, {}):
                    if name == sdef.name or name in _related_nr_names(sdef.name):
                        nr_names_needed.add(name)

    lines.append("/* --- Syscall numbers --- */\n\n")

    related = _related_nr_names(sdef.name)
    all_oses = sorted(OPERATING_SYSTEMS.keys())
    first_os = True
    for os_name in all_oses:
        os_macro = f"PICBLOBS_OS_{os_name.upper()}"
        prefix = "#if" if first_os else "#elif"
        lines.append(f"{prefix} defined({os_macro})\n\n")

        if os_name not in os_support:
            lines.append(f'#error "{sdef.wrapper}() is not available on {os_name}"\n\n')
        else:
            arch_nrs = os_support[os_name]
            if "_all_" in arch_nrs:
                nr = arch_nrs["_all_"]
                lines.append(f"#define __NR_{sdef.name:<16s} {nr}\n\n")
            else:
                # Per-architecture: emit all __NR_ defines this arch needs.
                defines = _sorted_gcc_defines()
                os_tables = SYSCALL_NUMBERS.get(os_name, {})
                first_arch = True
                for gcc_define in defines:
                    arch_table = os_tables.get(gcc_define, {})
                    # Collect all related __NR_ defines for this arch.
                    nr_defs: list[tuple[str, int]] = []
                    for name in sorted(related):
                        if name in arch_table:
                            nr_defs.append((name, arch_table[name]))
                    if nr_defs:
                        aprefix = "#if" if first_arch else "#elif"
                        lines.append(f"{aprefix} defined({gcc_define})\n")
                        for name, nr in nr_defs:
                            lines.append(f"#define __NR_{name:<16s} {nr}\n")
                        first_arch = False
                if not first_arch:
                    lines.append("#else\n")
                    lines.append(
                        f'#error "Unsupported architecture for {sdef.wrapper}()"\n'
                    )
                    lines.append("#endif\n\n")

        first_os = False

    lines.append("#else\n")
    lines.append(
        f'#error "No target OS defined — #include picblobs/os/{{os}}.h before sys/{sdef.name}.h"\n'
    )
    lines.append("#endif\n\n")

    # --- Constants ---
    if sdef.constants:
        lines.append("/* --- Constants --- */\n")
        lines.append(sdef.constants + "\n\n")

    # Special: mmap flags are arch+OS-specific.
    if sdef.name == "mmap":
        lines.append("/* mmap flags */\n")
        lines.append("#if defined(PICBLOBS_OS_LINUX) && defined(__mips__)\n")
        for flag in ("MAP_SHARED", "MAP_PRIVATE", "MAP_FIXED", "MAP_ANONYMOUS"):
            val = MMAP_FLAGS[flag].get("__mips__", MMAP_FLAGS[flag]["_default_"])
            lines.append(f"#define PIC_{flag:<16s} {val:#05x}\n")
        lines.append("#elif defined(PICBLOBS_OS_FREEBSD)\n")
        for flag in ("MAP_SHARED", "MAP_PRIVATE", "MAP_FIXED", "MAP_ANONYMOUS"):
            val = MMAP_FLAGS[flag].get("freebsd", MMAP_FLAGS[flag]["_default_"])
            lines.append(f"#define PIC_{flag:<16s} {val:#06x}\n")
        lines.append("#else\n")
        for flag in ("MAP_SHARED", "MAP_PRIVATE", "MAP_FIXED", "MAP_ANONYMOUS"):
            val = MMAP_FLAGS[flag]["_default_"]
            lines.append(f"#define PIC_{flag:<16s} {val:#04x}\n")
        lines.append("#endif\n\n")

    # --- Wrapper function ---
    lines.append("/* --- Wrapper --- */\n")
    if sdef.custom_body:
        lines.append(sdef.custom_body + "\n")
    else:
        attrs = ""
        if sdef.noreturn:
            attrs = "__attribute__((noreturn))\n"
        lines.append(
            f"{attrs}static inline {sdef.return_type} {sdef.wrapper}({sdef.params}) {{\n"
        )
        call = f"pic_syscall{sdef.arg_count}(__NR_{sdef.name}, {sdef.call_args})"
        if sdef.return_type == "void":
            lines.append(f"    {call};\n")
            if sdef.noreturn:
                lines.append("    __builtin_unreachable();\n")
        elif sdef.return_type == "void *":
            lines.append(f"    return (void *){call};\n")
        else:
            lines.append(f"    return {call};\n")
        lines.append("}\n")

    if sdef.hosted_call:
        lines.append("\n#endif /* !PIC_PLATFORM_HOSTED */\n")
    lines.append(f"#endif /* {guard} */\n")
    return "".join(lines)


def _related_nr_names(syscall_name: str) -> set[str]:
    """Return related __NR_ names for a syscall (e.g., open → openat)."""
    related: dict[str, set[str]] = {
        "open": {"open", "openat"},
        "lseek": {"lseek", "llseek"},
    }
    return related.get(syscall_name, {syscall_name})


