#!/usr/bin/env python3
"""Generate ALL derived files from the canonical registry.

This is the single codegen entry point. Every file it generates is
marked with a DO NOT EDIT header. Adding a new architecture, OS, or
syscall should NEVER require modifying a generated file.

Generated files:
  C headers:
    - src/include/picblobs/arch.h
    - src/include/picblobs/syscall.h          (dispatcher → per-arch files)
    - src/include/picblobs/picblobs.h         (auto-discovers sys/*.h)
    - src/include/picblobs/sys/{os}/nr.h      (for each OS in registry)

  Bazel:
    - platforms/BUILD.bazel
    - toolchains/BUILD.bazel

  Config:
    - .bazelrc  (platform config block between markers)

  C test runners:
    - tests/runners/{os}/runner.c             (dispatcher → per-arch start/*.h)

Usage:
    python tools/generate.py              # generate all
    python tools/generate.py --check      # exit 1 if any file is stale
"""

